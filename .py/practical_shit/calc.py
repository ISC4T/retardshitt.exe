# INPUTS
print("_______________________________________________")
Num_1 = input("Num 1: ")
Type = input("Type, +, -, *, /: ")
Num_2 = input("Num 2: ")
print("_______________________________________________")

# AWNSERS
if Type == "+":
    print(("Awnser is: "), (float(Num_1)) + (float(Num_2)))

elif Type == "-": 
    print(("Awnser is: "), (float(Num_1)) - (float(Num_2)))

elif Type == "*": 
    print(("Awnser is: "), (float(Num_1)) * (float(Num_2)))

elif Type == "/": 
    print(("Awnser is: "), (float(Num_1)) / (float(Num_2)))
print("_______________________________________________")